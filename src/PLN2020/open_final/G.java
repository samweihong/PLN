package PLN2020.open_final;

import java.util.Scanner;

public class G {
    static Scanner sc = new Scanner(System.in);

    public static void solution() {
        int T = sc.nextInt();
        System.out.println(T-1);
    }

    public static void main(String[] args) {
        int N = sc.nextInt();
        for (int i = 0; i < N; i++) {
            solution();
        }
    }
}
